public class ToanHoc {

    public static void main(String[] args) {
        int x = 10;
        long y = 100l;
        double k = 4.5;
        double tong = x + y + k;
        double hieu = y - x - k;
        double tich = x * y * k;
        double thuong = y / x / k;
        System.out.println(tong);
        System.out.println(hieu);
        System.out.println(tich);
        System.out.println(thuong);

    }
}
